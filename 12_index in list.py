# in python list to access the elements of list you had to specify the index number of that list using a number inside square brackets


name = ['sunday','monday','tuesday','wednesday','tursday','friday']

# printing name index 0 as a title
print('printing name at index 0 like title:', name[0].title())


# printing name index 1 in upper case
print('\n\name at index 1 in upper case:',name[1].upper())


# printing name index 2 in lowercase
print('\n\nname at index 2 in lower case:',name[2].lower())

# in python index[-1] will return the last item of a list
print('\n\nlast item using name[-1]:')
print(name[-1])