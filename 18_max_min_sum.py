#simple statistices with a list of numbers
#using min, max, and sum methods on a list

#a list of numbers from 1-10
numbers = list(range(1,11))

#min: will display the minimum number of the list
print('\n',min(numbers))

#max: will display the maximum number in a list
print('\n',max(numbers))

#sum: summation of all digits in a list
print('\n',sum(numbers))